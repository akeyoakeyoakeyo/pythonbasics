# comparison operator
no1 = int(input("Enter a number:"))
no2 = int(input("Enter another number:"))
print(f"{no2} is equal to {no1} {no1 == no2}")
print(f"{no2} is not equal to {no1} {no1 != no2}")
print(f"{no1} is greater than {no2} {no1>no2}")

